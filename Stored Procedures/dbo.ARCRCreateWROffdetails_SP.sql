CREATE PROC [dbo].[ARCRCreateWROffdetails_SP]	@batch_ctrl_num     varchar( 16 ),
                                		@debug_level        smallint = 0,
                                		@perf_level         smallint = 0    
AS








DECLARE
        @PERF_time_last     datetime

SELECT  @PERF_time_last = GETDATE()

















									







DECLARE
    	@result             	int

IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, "arcrcwro.cpp", 67, "Entering ARCRCreateWROffdetails_SP", @PERF_time_last OUTPUT










BEGIN

	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "arcrcwro.cpp" + ", line " + STR( 80, 5 ) + " -- ENTRY: "

	

	


	










































		



	
		











			INSERT	#argldist
			(
				date_applied,				account_code,		
				description,				document_1,
				document_2,				nat_balance,				
				nat_cur_code,				rate_type_home,			
				rate_type_oper,				rate_home,				
				rate_oper,				trx_type,				
				seq_ref_id,				trx_ctrl_num,
                                org_id        
			)
			SELECT	
				pyt.date_applied,			dbo.IBAcctMask_fn(wrof.writeoff_account,pdt.org_id),   
				trx_desc,				pyt.customer_code,
				pyt.doc_ctrl_num,			pdt.inv_amt_wr_off,
				pdt.inv_cur_code,	  		artrx.rate_type_home,
				artrx.rate_type_oper,			artrx.rate_home,
				artrx.rate_oper,			pyt.trx_type,
				0,					pyt.trx_ctrl_num,
                                pdt.org_id    
			FROM	#artrxpdt_work pdt, #arinppyt_work pyt, #artrx_work artrx, arwrofac wrof (NOLOCK)
			WHERE		
				pdt.trx_ctrl_num   = pyt.trx_ctrl_num
			AND	pdt.sub_apply_num  = artrx.doc_ctrl_num
			AND	pdt.sub_apply_type = artrx.trx_type
			AND     pdt.writeoff_code  = wrof.writeoff_code
			AND     pyt.non_ar_flag   = 0
                        AND	pyt.batch_code     = @batch_ctrl_num

	
	IF (@debug_level > 0)
		BEGIN
			SELECT "dumping argldist after Writeoff details are added"
			SELECT	"date_applied journal_type rec_company_code account code description document_1 document_2"
			SELECT	STR(date_applied, 7) + ":" +
					journal_type + ":" +
					rec_company_code + ":" +
					account_code + ":" +
					description + ":" +
					document_1 + ":" +
					document_2
			FROM	#argldist
					
			SELECT	"document_2 reference_code home_balance home_cur_code nat_balance nat_cur_code rate trx_type seq_ref_id"
			SELECT	document_2 + ":" +
					reference_code + ":" +
					STR(home_balance, 10, 4) + ":" +
					home_cur_code + ":" +
					STR(nat_balance, 10, 4) + ":" +
					nat_cur_code + ":" +
					STR(rate_home, 10, 6) + ":" +
					STR(trx_type, 5 ) + ":" +
					STR(seq_ref_id, 6)
			FROM	#argldist
	END


IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "arcrcwro.cpp" + ", line " + STR( 204, 5 ) + " -- EXIT: "
IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, "arcrcwro.cpp", 205, "Leaving ARCRCreateWROffdetails_SP", @PERF_time_last OUTPUT
RETURN 0 

END

GO
GRANT EXECUTE ON  [dbo].[ARCRCreateWROffdetails_SP] TO [public]

CREATE PROC [dbo].[ARCMUpdateActivitySummary_SP]	@batch_ctrl_num	varchar( 16 ),
						@debug_level		smallint,
						@perf_level		smallint
AS






DECLARE
 @PERF_time_last datetime

SELECT @PERF_time_last = GETDATE()















									







DECLARE
	@result 			int

IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, "tmp/arcmuas.sp", 54, "Entering ARCMUpdateActivitySummary_SP", @PERF_time_last OUTPUT

BEGIN
	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arcmuas.sp" + ", line " + STR( 57, 5 ) + " -- ENTRY: "

	EXEC @result = ARCMUpdateActivity_SP	@batch_ctrl_num,
							@debug_level,
							@perf_level
	IF( @result != 0 )
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arcmuas.sp" + ", line " + STR( 64, 5 ) + " -- EXIT: "
		RETURN @result
	END

	EXEC @result = ARCMUpdateSummary_SP	@batch_ctrl_num,
							@debug_level,
							@perf_level
	IF( @result != 0 )
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arcmuas.sp" + ", line " + STR( 73, 5 ) + " -- EXIT: "
		RETURN @result
	END

	IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, "tmp/arcmuas.sp", 77, "Leaving ARCMUpdateActivitySummary_SP", @PERF_time_last OUTPUT
	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arcmuas.sp" + ", line " + STR( 78, 5 ) + " -- EXIT: "
	RETURN 0 
END
GO
GRANT EXECUTE ON  [dbo].[ARCMUpdateActivitySummary_SP] TO [public]
GO

CREATE PROC [dbo].[ARCACreateDiscTDetails_SP]	@batch_ctrl_num     varchar( 16 ),
                                		@debug_level        smallint = 0,
                                		@perf_level         smallint = 0    
AS








DECLARE
        @PERF_time_last     datetime

SELECT  @PERF_time_last = GETDATE()

















									








IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, 'arcacdtd.cpp', 50, 'Entering ARCACreateDiscTDetails_SP', @PERF_time_last OUTPUT













BEGIN
	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + '  ' + 'arcacdtd.cpp' + ', line ' + STR( 65, 5 ) + ' -- ENTRY: '


	





	INSERT	#argldist
	(
		date_applied,				account_code,		
		description,				document_1,
		document_2,				nat_balance,				
		nat_cur_code,				rate_type_home,			
		rate_type_oper,			rate_home,				
		rate_oper,				trx_type,				
		seq_ref_id,				trx_ctrl_num,
		org_id
	)
	SELECT	pyt.date_applied,			dbo.IBAcctMask_fn(acct.disc_taken_acct_code,trx.org_id), 		
		pyt.trx_desc,				pyt.customer_code,
		pyt.doc_ctrl_num,			-pdt.inv_amt_disc_taken,			
		trx.nat_cur_code,			trx.rate_type_home,			
		trx.rate_type_oper,			trx.rate_home,			
		trx.rate_oper,			pyt.trx_type,				
		0,					pyt.trx_ctrl_num,
		trx.org_id 										
	FROM	#arinppdt_work pdt, #arinppyt_work pyt, #artrx_work trx, araccts acct
	WHERE	pdt.trx_ctrl_num = pyt.trx_ctrl_num
	AND	pdt.sub_apply_num = trx.doc_ctrl_num
	AND	pdt.sub_apply_type = trx.trx_type
	AND	trx.posting_code = acct.posting_code
	AND	pyt.batch_code = @batch_ctrl_num

	IF( @@error != 0 )
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + '  ' + 'arcacdtd.cpp' + ', line ' + STR( 102, 5 ) + ' -- EXIT: '
		RETURN 34563
	END

		

IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + '  ' + 'arcacdtd.cpp' + ', line ' + STR( 108, 5 ) + ' -- EXIT: '
IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, 'arcacdtd.cpp', 109, 'Leaving ARCACreateDiscTDetails_SP', @PERF_time_last OUTPUT
RETURN 0 

END

GO

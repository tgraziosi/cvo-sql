CREATE PROCEDURE [dbo].[arnewnum_sp]	@trx_type	smallint,
					@next_tcn	varchar(16) OUTPUT


AS DECLARE	@mask		varchar(16), 
		@next_number	int,
		@result	smallint,
		@tran_started	smallint,
		@num_type	int

BEGIN
	IF (@trx_type = 2031)
		SELECT @num_type = 2000
	
	ELSE IF (@trx_type = 2051)
		SELECT @num_type = 2080
	
	ELSE IF (@trx_type IN (2111, 2112, 2113, 2121))
		SELECT @num_type = 2010
	
	ELSE IF (@trx_type = 2151)
		SELECT @num_type = 2030
	
	ELSE IF (@trx_type = 2032)
		SELECT @num_type = 2020

	IF ( @@trancount = 0 )
	BEGIN
		BEGIN TRAN
		SELECT	@tran_started = 1
	END

	EXEC @result = ARGetNextControl_SP		@num_type,
							@next_tcn OUTPUT,
							@next_number OUTPUT
	IF ( @result != 0 )
	BEGIN
		IF ( @tran_started = 1 )
			ROLLBACK TRAN

		SELECT	@next_tcn = NULL

		RETURN	32500
	END

	IF ( @tran_started = 1 )
		COMMIT TRAN

	RETURN 0
END
GO
GRANT EXECUTE ON  [dbo].[arnewnum_sp] TO [public]

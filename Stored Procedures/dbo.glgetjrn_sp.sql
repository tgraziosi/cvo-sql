CREATE PROCEDURE [dbo].[glgetjrn_sp]
		@journal_type		varchar(8) OUTPUT,
 	@mid	 		smallint,
		@trx_type		smallint


AS 
BEGIN
	SELECT	@journal_type = NULL
	
	SELECT	@journal_type = journal_type
	FROM	glappid
	WHERE	app_id = @mid
		
	IF ( @journal_type IS NULL )
	BEGIN
		RETURN 	1003
	END
	
	RETURN 0
END



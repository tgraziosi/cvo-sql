CREATE PROC [dbo].[ARIACreateInvAdjDetails_SP]	@batch_ctrl_num     varchar( 16 ),
                                		@debug_level        smallint = 0,
                                		@perf_level         smallint = 0    
AS








DECLARE
        @PERF_time_last     datetime

SELECT  @PERF_time_last = GETDATE()

















									








IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, 'ariaciad.cpp', 47, 'Entering ARIACreateInvAdjDetails_SP', @PERF_time_last OUTPUT

BEGIN
	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + '  ' + 'ariaciad.cpp' + ', line ' + STR( 50, 5 ) + ' -- ENTRY: '

	



	INSERT	#argldist
	(
		date_applied,				account_code,		
		description,				document_1,
		document_2,				nat_balance,				
		nat_cur_code,				rate_type_home,			
		rate_type_oper,			rate_home,				
		rate_oper,				trx_type,				
		seq_ref_id,				trx_ctrl_num,
		reference_code,				org_id	
	)
	SELECT	chg.date_applied,			cdt.gl_rev_acct,	
		chg.doc_desc,				chg.customer_code,
		chg.doc_ctrl_num,			(cdt.extended_price + cdt.discount_amt - (SIGN(tx.tax_included_flag) * cdt.calc_tax)),				
		chg.nat_cur_code,			chg.rate_type_home,			
		chg.rate_type_oper,			chg.rate_home,			
		chg.rate_oper,			chg.trx_type,				
		cdt.sequence_id,			chg.trx_ctrl_num,
		cdt.reference_code,			cdt.org_id
	FROM	#arinpchg_work chg, #arinpcdt_work cdt, artax tx
	WHERE	chg.trx_ctrl_num = cdt.trx_ctrl_num
	AND	chg.trx_type = cdt.trx_type
	AND	cdt.tax_code = tx.tax_code
	AND	chg.batch_code = @batch_ctrl_num
	IF( @@error != 0 )
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + '  ' + 'ariaciad.cpp' + ', line ' + STR( 82, 5 ) + ' -- EXIT: '
		RETURN 34563
	END

	

		
	INSERT	#argldist
	(
		date_applied,				account_code,		
		description,				document_1,
		document_2,				nat_balance,				
		nat_cur_code,				rate_type_home,			
		rate_type_oper,			rate_home,				
		rate_oper,				trx_type,				
		seq_ref_id,				trx_ctrl_num,
		reference_code,				org_id	
	)
	SELECT	chg.date_applied,			cdt.new_gl_rev_acct,	
		chg.doc_desc,				chg.customer_code,
		chg.doc_ctrl_num,			-(cdt.extended_price + cdt.discount_amt - (SIGN(tx.tax_included_flag) * cdt.calc_tax)),				
		chg.nat_cur_code,			chg.rate_type_home,			
		chg.rate_type_oper,			chg.rate_home,			
		chg.rate_oper,			chg.trx_type,				
		cdt.sequence_id,			chg.trx_ctrl_num,
		cdt.new_reference_code,			cdt.org_id
	FROM	#arinpchg_work chg, #arinpcdt_work cdt, artax tx
	WHERE	chg.trx_ctrl_num = cdt.trx_ctrl_num
	AND	chg.trx_type = cdt.trx_type
	AND	cdt.tax_code = tx.tax_code
	AND	chg.batch_code = @batch_ctrl_num
	IF( @@error != 0 )
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + '  ' + 'ariaciad.cpp' + ', line ' + STR( 115, 5 ) + ' -- EXIT: '
		RETURN 34563
	END
	
	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + '  ' + 'ariaciad.cpp' + ', line ' + STR( 119, 5 ) + ' -- EXIT: '
	IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, 'ariaciad.cpp', 120, 'Leaving ARCACreateCashDetails_SP', @PERF_time_last OUTPUT
	RETURN 0 

END

GO

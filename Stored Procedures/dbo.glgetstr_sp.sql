CREATE PROCEDURE [dbo].[glgetstr_sp]
 	@strid	 		int,
		@string			varchar(80) OUTPUT


AS 
BEGIN
	SELECT	@string = str_text
	FROM	glstrdef
	WHERE	str_id = @strid
	
	IF ( @string IS NULL )
	BEGIN
		RETURN 	3006
	END
	
	RETURN 0
END



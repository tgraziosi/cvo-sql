											@client_id 			varchar(20),
											@user_id			int, 
											@debug_level		smallint = 0
	
AS

DECLARE
	@errbuf				varchar(100),
	@company_code varchar(8),
	@cm_flag smallint,
	@result				int

IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/appyup.sp" + ", line " + STR( 65, 5 ) + " -- ENTRY: "

	

	EXEC @result = APPYModifyPersistant_sp	@batch_ctrl_num,
											@client_id,
											@user_id,
											@process_group_num,
											@debug_level
	IF( @result != 0 )
 RETURN @result

	
	SELECT @company_code = company_code from glco 

	EXEC @result = gltrxsav_sp @process_group_num, 
								@company_code
	IF( @result != 0 )
		RETURN -1

 SELECT @cm_flag = bb_flag
	FROM apco

 IF(@cm_flag = 1)
	BEGIN
		EXEC @result = cminpsav_sp
		IF( @result != 0 )
			RETURN -1
	END


	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/appyup.sp" + ", line " + STR( 101, 5 ) + " -- EXIT: "
	RETURN 0
GO

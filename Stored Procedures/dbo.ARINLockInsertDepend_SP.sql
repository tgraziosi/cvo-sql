						@process_ctrl_num	varchar( 16 ),
						@batch_proc_flag	smallint,
						@debug_level		smallint = 0,
						@perf_level		smallint = 0 
AS






DECLARE
 @PERF_time_last datetime

SELECT @PERF_time_last = GETDATE()















									







DECLARE
	@result		int,
	@trans_unlocked_flag	int

IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, "tmp/arinlid.sp", 72, "Entering ARINLockInsertDepend_SP", @PERF_time_last OUTPUT

BEGIN
	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arinlid.sp" + ", line " + STR( 75, 5 ) + " -- ENTRY: "

	
	EXEC @result = ARINLockDependancies_SP	@batch_ctrl_num,
							@process_ctrl_num,
							@batch_proc_flag,
							@debug_level,
							@perf_level
	IF( @result != 0 )
	BEGIN
		IF ( @debug_level > 2 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arinlid.sp" + ", line " + STR( 87, 5 ) + " -- MSG: " + "ARINLockDependancies_SP has failed"
		IF ( @debug_level > 2 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arinlid.sp" + ", line " + STR( 88, 5 ) + " -- MSG: " + "@result = " + STR( @result, 7 )
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arinlid.sp" + ", line " + STR( 89, 5 ) + " -- EXIT: "
		RETURN @result
	END

	
	EXEC @result = ARINInsertDependancies_SP	@batch_ctrl_num,
							@process_ctrl_num,
							@debug_level,
							@perf_level
	IF( @result != 0 )
	BEGIN
		IF ( @debug_level > 2 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arinlid.sp" + ", line " + STR( 102, 5 ) + " -- MSG: " + "ARINLockDependancies_SP has failed"
		IF ( @debug_level > 2 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arinlid.sp" + ", line " + STR( 103, 5 ) + " -- MSG: " + "@result = " + STR( @result, 7 )
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arinlid.sp" + ", line " + STR( 104, 5 ) + " -- EXIT: "
		RETURN @result
	END

	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arinlid.sp" + ", line " + STR( 108, 5 ) + " -- EXIT: "
	IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, "tmp/arinlid.sp", 109, "Leaving ARINLockInsertDepend_SP", @PERF_time_last OUTPUT
	RETURN 0
END









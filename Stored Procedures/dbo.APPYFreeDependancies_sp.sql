CREATE PROC [dbo].[APPYFreeDependancies_sp]
											@batch_ctrl_num 	varchar(16),
											@client_id 			varchar(20),
											@user_id			smallint, 
											@process_group_num 	varchar(16),
											@debug_level		smallint = 0
	
AS
DECLARE
	@result int

IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/appyfd.sp" + ", line " + STR( 51, 5 ) + " -- ENTRY: "


UPDATE	apvohdr
SET	state_flag = 1,
	process_ctrl_num = " "
FROM	apvohdr
WHERE 	state_flag = -1
 AND	process_ctrl_num = @process_group_num

IF(@@error != 0)
	RETURN -1


UPDATE	appyhdr
SET	state_flag = 1,
	process_ctrl_num = " "
FROM	appyhdr
WHERE 	state_flag = -1
 AND	process_ctrl_num = @process_group_num

IF(@@error != 0)
	RETURN -1

IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/appyfd.sp" + ", line " + STR( 77, 5 ) + " -- EXIT: "

RETURN 0
GO
GRANT EXECUTE ON  [dbo].[APPYFreeDependancies_sp] TO [public]
GO

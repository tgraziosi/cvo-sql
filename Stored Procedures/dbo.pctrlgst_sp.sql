CREATE PROCEDURE [dbo].[pctrlgst_sp] 
			@process_ctrl_num varchar(16),
			@process_status varchar(80) OUTPUT
AS

BEGIN

	SELECT @process_status = process_status
	FROM pcontrol_vw
	WHERE process_ctrl_num = @process_ctrl_num

	IF ( @@rowcount = 0 )
		RETURN -1

	RETURN 0
END
GO
GRANT EXECUTE ON  [dbo].[pctrlgst_sp] TO [public]

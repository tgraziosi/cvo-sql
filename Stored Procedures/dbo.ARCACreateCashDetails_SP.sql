CREATE PROC [dbo].[ARCACreateCashDetails_SP]		@batch_ctrl_num     varchar( 16 ),
                                			@debug_level        smallint = 0,
                                			@perf_level         smallint = 0    
AS








DECLARE
        @PERF_time_last     datetime

SELECT  @PERF_time_last = GETDATE()

















									








IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, 'arcaccd.cpp', 50, 'Entering ARCACreateCashDetails_SP', @PERF_time_last OUTPUT










BEGIN
	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + '  ' + 'arcaccd.cpp' + ', line ' + STR( 62, 5 ) + ' -- ENTRY: '

	


	INSERT	#argldist
	(
		date_applied,				account_code,		
		description,				document_1,
		document_2,				nat_balance,				
		nat_cur_code,				rate_type_home,			
		rate_type_oper,			rate_home,				
		rate_oper,				trx_type,				
		seq_ref_id,				trx_ctrl_num,
		org_id
	)
	SELECT	date_applied,				cash_acct_code,	
		trx_desc,				customer_code,
		doc_ctrl_num,				-amt_payment,			
		nat_cur_code,				rate_type_home,			
		rate_type_oper,			rate_home,			
		rate_oper,				trx_type,				
		0,					trx_ctrl_num,
		org_id									
	FROM	#arinppyt_work 
	WHERE	batch_code = @batch_ctrl_num
	AND	trx_type between 2113 and 2121
	IF( @@error != 0 )
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + '  ' + 'arcaccd.cpp' + ', line ' + STR( 91, 5 ) + ' -- EXIT: '
		RETURN 34563
	END


	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + '  ' + 'arcaccd.cpp' + ', line ' + STR( 96, 5 ) + ' -- EXIT: '
	IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, 'arcaccd.cpp', 97, 'Leaving ARCACreateCashDetails_SP', @PERF_time_last OUTPUT
	RETURN 0 

END

GO
GRANT EXECUTE ON  [dbo].[ARCACreateCashDetails_SP] TO [public]

CREATE PROCEDURE [dbo].[ARFLValidate_SP]	@batch_ctrl_num	varchar( 16 ),
					@debug_level		smallint = 0,
					@perf_level		smallint = 0	
AS






DECLARE
 @PERF_time_last datetime

SELECT @PERF_time_last = GETDATE()















									







DECLARE
	@result		int,
	@process_ctrl_num	varchar( 16 )

IF ( @perf_level >= 1 ) EXEC perf_sp @batch_ctrl_num, "tmp/arflv.sp", 39, "ARFLValidate_SP", @PERF_time_last OUTPUT

BEGIN
	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arflv.sp" + ", line " + STR( 42, 5 ) + " -- ENTRY: "
	
	EXEC @result = ARFLValidatePostingCode_SP	@batch_ctrl_num,
							@debug_level,
							@perf_level
							
	IF ( @result != 0 )
		RETURN @result
	
	
	SELECT	@process_ctrl_num = p.process_ctrl_num
	FROM	batchctl b, pcontrol_vw p
	WHERE	b.process_group_num = p.process_ctrl_num
	AND	b.batch_ctrl_num = @batch_ctrl_num

		
	EXEC @result = ARInsertPERRors_SP 	@process_ctrl_num,
						@batch_ctrl_num,
						@debug_level
						
	IF ( @result != 0 )
		RETURN @result
		
	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arflv.sp" + ", line " + STR( 69, 5 ) + " -- EXIT: "
	RETURN 0
END
GO
GRANT EXECUTE ON  [dbo].[ARFLValidate_SP] TO [public]
GO

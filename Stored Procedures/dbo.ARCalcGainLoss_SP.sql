CREATE PROC [dbo].[ARCalcGainLoss_SP]	@debug_level smallint = 0,
 	@perf_level smallint = 0 
AS
DECLARE	@mc_flag	smallint,
		@result	smallint

BEGIN
	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arcgl.sp" + ", line " + STR( 36, 5 ) + " -- ENTRY: "

	
	EXEC @result = ARLoadHomeOper_SP	@debug_level,
						@perf_level
	IF (@result != 0)
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arcgl.sp" + ", line " + STR( 46, 5 ) + " -- EXIT: "
		RETURN @result
	END

	SELECT	@mc_flag = mc_flag
	FROM	arco

	
	IF ( @mc_flag > 0 )
	BEGIN
		SELECT @result = 0
	END

	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/arcgl.sp" + ", line " + STR( 61, 5 ) + " -- EXIT: "
	RETURN 0
END
GO

CREATE PROCEDURE	[dbo].[arflmkbt_sp]	@process_ctrl_num	varchar(16),
					@company_code		varchar(8),
					@debug_level		smallint = 0
AS

BEGIN
	DECLARE 	@process_user_id	smallint,  
			@process_parent_app	smallint,   
			@result		int,
			@batch_ctrl_num	varchar(16),
			@date_applied		int
	

	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 43, 5 ) + " -- ENTRY: "
	IF ( @debug_level > 2 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 44, 5 ) + " -- MSG: " + " Passed parameter: process_control_num = " + @process_ctrl_num
	IF ( @debug_level > 2 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 45, 5 ) + " -- MSG: " + " Passed parameter: company_code = " + @company_code

	EXEC appdate_sp @date_applied OUTPUT
	
	



	IF ( @@trancount > 0 )
	BEGIN
		IF ( @debug_level > 2 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 55, 5 ) + " -- MSG: " + "Transaction has already been started"
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 56, 5 ) + " -- EXIT: "
		RETURN 32544
	END
	


	SELECT	@process_parent_app = process_parent_app,
		@process_user_id = process_user_id
	FROM	pcontrol_vw
	WHERE	process_ctrl_num = @process_ctrl_num
	
	IF ( @process_parent_app IS NULL )
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 69, 5 ) + " -- EXIT: "
		RETURN	32545
	END
	
	


	IF NOT EXISTS(SELECT	*
			FROM	glcomp_vw
			WHERE	company_code = @company_code )
			
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 81, 5 ) + " -- EXIT: "
		RETURN	32523
	END
	
	IF ( @debug_level > 2 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 85, 5 ) + " -- MSG: " + "Calling arnxtbat_sp"
	
	DECLARE @org_id varchar(30)
	SELECT @org_id = dbo.sm_get_current_org_fn()

	EXEC @result = arnxtbat_sp	2000,
	                		"",
				  	2080,
				  	@process_user_id,
					@date_applied,
					@company_code,
					@batch_ctrl_num OUTPUT,	
					@debug_level,
					@org_id	
					
	IF ( @result != 0 )
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 102, 5 ) + " -- EXIT: "
		RETURN	32502
	END
		
	IF ( @debug_level > 2 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 106, 5 ) + " -- MSG: " + "Beginning Transaction"
	BEGIN TRAN

	



	UPDATE	batchctl
	SET	process_group_num = @process_ctrl_num,
	    	posted_flag = -1
	WHERE	batch_ctrl_num = @batch_ctrl_num

	IF ( @debug_level > 2 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 118, 5 ) + " -- MSG: " + "Commiting Transaction"
	COMMIT TRAN

	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 121, 5 ) + " -- EXIT: "
	RETURN 0
	
rollback_tran:
	IF ( @debug_level > 2 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 125, 5 ) + " -- MSG: " + "Rolling Back transaction"
	ROLLBACK TRAN
	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "arflmkbt.cpp" + ", line " + STR( 127, 5 ) + " -- EXIT: "
	RETURN	32502
END

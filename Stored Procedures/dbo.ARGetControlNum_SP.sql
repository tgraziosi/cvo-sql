CREATE PROCEDURE [dbo].[ARGetControlNum_SP] 	@num_type 		int, 
										@num 			int OUTPUT, 
										@debug_level 	int = 0

AS
DECLARE @mask 	char(35),
		@tran_started int

BEGIN

	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/argcn.sp" + ", line " + STR( 46, 5 ) + " -- ENTRY: "



	IF (@@trancount = 0)
	BEGIN
		SELECT @tran_started = 1
		BEGIN TRANSACTION
	END
	ELSE
		SELECt @tran_started = 0



	UPDATE	ewnumber
	SET		next_num = next_num + 1
	WHERE	num_type = @num_type



	IF (@@error != 0)
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/argcn.sp" + ", line " + STR( 74, 5 ) + " -- EXIT: "
		IF (@tran_started = 1)
		BEGIN
			ROLLBACK TRANSACTION
		END
		RETURN -1
	END



	SELECT 	@num = next_num - 1
	FROM	ewnumber
	WHERE	num_type = @num_type



	IF (@@error != 0)
	BEGIN
		IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/argcn.sp" + ", line " + STR( 96, 5 ) + " -- EXIT: "
		IF (@tran_started = 1)
		BEGIN
			ROLLBACK TRANSACTION
		END
		RETURN -1
	END



	IF (@tran_started = 1)
	BEGIN
		COMMIT TRANSACTION
	END



	IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/argcn.sp" + ", line " + STR( 117, 5 ) + " -- EXIT: "	

	RETURN 0
	
END 


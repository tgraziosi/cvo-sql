CREATE PROCEDURE [dbo].[apdbmedt_sp] @only_errors smallint, @debug_level smallint = 0
AS

DECLARE
 @result int, @error_level smallint


IF ( @debug_level > 1 ) SELECT CONVERT(char,getdate(),109) + "  " + "tmp/apdbmedt.sp" + ", line " + STR( 26, 5 ) + " -- ENTRY: "

IF @only_errors = 1
 SELECT @error_level = 0
ELSE 
	SELECT @error_level = 1


EXEC @result = apdehdr1_sp @error_level, @debug_level

EXEC @result = apdehdr2_sp @error_level, @debug_level

EXEC @result = apdehdr3_sp @error_level, @debug_level

EXEC @result = apdehdr4_sp @error_level, @debug_level


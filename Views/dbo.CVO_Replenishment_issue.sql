CREATE VIEW [dbo].[CVO_Replenishment_issue]
AS
SELECT     a.part_no, a.bin_no, SUM(a.qty) AS allocated_qty, b.qty, c.replenish_min_lvl, c.replenish_max_lvl
FROM         dbo.tdc_soft_alloc_tbl AS a INNER JOIN
                      dbo.lot_bin_stock AS b ON a.part_no = b.part_no AND a.bin_no = b.bin_no INNER JOIN
                      dbo.tdc_bin_replenishment AS c ON a.part_no = c.part_no AND a.bin_no = c.bin_no
WHERE     (a.location = '001') AND (c.location = '001')
GROUP BY a.part_no, a.bin_no, b.qty, c.replenish_min_lvl, c.replenish_max_lvl
HAVING      (SUM(a.qty) >= b.qty)
